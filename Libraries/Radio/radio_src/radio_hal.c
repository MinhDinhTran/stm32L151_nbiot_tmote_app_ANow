#include "radio_hal.h"
#include "hal_rf.h"

/**********************************************************************************************************
 @Function			void Radio_Hal_AssertShutdown(void)
 @Description			Radio_Hal_AssertShutdown			: 置位SDN
 @Input				void
 @Return				void
**********************************************************************************************************/
void Radio_Hal_AssertShutdown(void)
{
	HAL_GPIO_WritePin(RF_SDN_GPIO_PORT, RF_SDN_PIN, GPIO_PIN_SET);
}


























